    public void Add(ArrayList<LuongModel> luongModel, DefaultTableModel table){
        for(LuongModel i : luongModel){
            Object[] obj = {i.getMaTK(), i.getTenNV(), i.getThang(), i.getNam(),i.getTongSoGioLamViec(), i.getLuong()};
            table.addRow(obj);
        }
    }
    
